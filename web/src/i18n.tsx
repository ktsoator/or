export type Locale = 'en' | 'zh-CN'

type I18nValue = {
  locale: Locale
  setLocale: (locale: Locale) => void
  t: (key: TranslationKey, values?: TranslationValues) => string
  formatNumber: (value: number, options?: Intl.NumberFormatOptions) => string
}

function initialLocale(): Locale {
  const saved = window.localStorage.getItem('or.locale')
  if (saved === 'en' || saved === 'zh-CN') return saved
  return window.navigator.language.toLowerCase().startsWith('zh') ? 'zh-CN' : 'en'
}

function normalizeLocale(language?: string): Locale {
  return language?.toLowerCase().startsWith('zh') ? 'zh-CN' : 'en'
}

function persistLocale(language: string) {
  const locale = normalizeLocale(language)
  document.documentElement.lang = locale
  window.localStorage.setItem('or.locale', locale)
}

const locale = initialLocale()

void i18n.use(initReactI18next).init({
  resources: {
    en: { translation: en },
    'zh-CN': { translation: zh },
  },
  lng: locale,
  fallbackLng: 'en',
  supportedLngs: ['en', 'zh-CN'],
  defaultNS: 'translation',
  keySeparator: false,
  initAsync: false,
  interpolation: {
    escapeValue: false,
    prefix: '{',
    suffix: '}',
  },
})

persistLocale(locale)
i18n.on('languageChanged', persistLocale)

export function useI18n(): I18nValue {
  const { t: translate, i18n: instance } = useTranslation()
  const currentLocale = normalizeLocale(instance.resolvedLanguage ?? instance.language)
  const setLocale = useCallback(
    (nextLocale: Locale) => {
      void instance.changeLanguage(nextLocale)
    },
    [instance],
  )
  const t = useCallback(
    (key: TranslationKey, values?: TranslationValues) => translate(key, values),
    [translate],
  )
  const formatNumber = useCallback(
    (value: number, options?: Intl.NumberFormatOptions) =>
      value.toLocaleString(currentLocale === 'zh-CN' ? 'zh-CN' : 'en-US', options),
    [currentLocale],
  )

  return { locale: currentLocale, setLocale, t, formatNumber }
}

export default i18n
